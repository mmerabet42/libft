#include "libft.h"

size_t	ft_strlcat(char *a, const char *b, size_t n)
{
	size_t	i;

	i = ft_strlen(a);
	while (*b && i < n)
		a[i++] = *b++;
	a[i] = '\0';
	return (i);
}
