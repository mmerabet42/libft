#include "libft.h"

int	ft_strchr_pos(const char *s, int c)
{
	int	pos;

	if (!s)
		return (-1);
	pos = 0;
	while (s[pos] && s[pos] != c)
		++pos;
	return ((s[pos] == '\0') ? -1 : pos);
}
